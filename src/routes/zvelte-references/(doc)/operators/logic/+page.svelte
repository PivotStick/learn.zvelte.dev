<p>You can combine multiple expressions with the following operators:</p>

<ul>
	<li>
		<code>and</code> Returns true if the left and the right operands are both true.
	</li>

	<li>
		<code>or, ||</code> Returns true if the left or the right operand is true.
	</li>

	<li>
		<code>not</code> Negates a statement.
	</li>

	<li>
		<code>(expr)</code> Groups an expression.
	</li>
</ul>
